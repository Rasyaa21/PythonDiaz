#operator perbandingan
#operator ini digunakan untuk membandingan sebuah nilai apakah nilai itu sama ataukah nilai itu lebih kecil atau lebih besar
a = 5
b = 5

#operator perbadingan (==)sama dengan dapat diartika jika nilai a sama dengan nilai b maka output dari program berupa true
#jika kondisi nilai a tidak sama dengan nilai b maka outputnya akan false
if (a == b):
    print(True)
else:
    print(False)

#operator perbandingan (!=) tidka sama dengan dapat diartika jika nilai a tidak sama dengan nilai b maka output dari program tersebut akan berupa true
#jika kondisi nilai a sama dengan nilai b maka outputnya akan false
if (a != b):
    print(True)
else:
    print(False)

#operator lebih dari dan kurang dari ini adalah operator yang sering kita dengar karena sering muncul dalam mapel matematika
#sama seperti di matematika operator ini dapat dijadikan perbandingan lebih besar atau lebih kecilnya nilai dari suatu variabel ini
if (a > b):
    print(True)
else:
    print(False)
#sama saja dengan operator kurang dari hanya kebalikannya dari operator lebih dari

#operator lebih dari sama dengan dan kurang dari sama dengan merupakan operator perbandingan yang mirip dengan lebih dari dan kurang dari
#contoh penggunaan
if (a >= b):
    print(True)
else:
    print(False)
#output dari program ini adalah true karena a sama dengan b jadi yang dicetak adalah true
