#Operator Penugasan
# operator penugasan adalah operator yang digunakan untuk memberikan suatu nilai kepada sebuah variabel
# ada dua sisi operand yang memiliki fungsinya masing masing

#Operand pada sisi kiri operator adalah variabel
#sedangkan operand pada sisi kanan operator merupakan sebuah nilai atau variabel yang memiliki nilai.

#contoh penggunaan dari operator penugasan
a = 2
a += 2 # (+=) mengartikan bahwa variabel a sama dengan variabel dari a itu sendiri yaitu dua ditambah dengan dua (a = a + 2)

a -= 2 # (-=) ini juga sama yang berarti variabel a sama dengan nilai dari variabel a sendiri dikurangi dengan 2
