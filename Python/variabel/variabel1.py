#variabel
#variabel digunakan sebagai penampung dari data
#bisa kita ibaratkan variabel adalah gelas kosong yang akan di isi dengan data

#contoh pendeklarasian variabel pada bahasa pemrograman python
nama = "rusdi"
#disini kita mendeklarasikan nama dari variabel tersebut yaitu nama
#dilanjutkan dengan apa yang akan ditampung dalam variabel tersebut
#bisa berupa string, int, bool, double dan lain sebagainya tanpa perlu
#menspesifikasikan tipe data dari variabel tersebut
print(nama)
#semisal kita ganti variabel nama yang sebelumnya berupa string kita bisa ganti ke int tanpa mengalami error
#inilah salah satu keunikan dari bahasa pemrograman python yang jarang ditemui di bahasa pemrograman lainnya
nama = 5

#cara menampilkan variabel pada output
print(nama)
#kita bisa melakukan print diikutin dengan nama variabelnya
#ini akan menampilkan isi dari variabel nama
