# multiple variabel
# di bahasa pemrograman python kita dapat mendekalarikan benyak variabel dalam 1 line
# contoh pendeklarasian 2 variable dalam 1 line
namaDepan, namabelakang = "Helmi" , "Suhaday"
#pada saat kita print hasil yang muncul sesuai dengan apa yang kita dekalarasikan
print(namaDepan, namabelakang)

#kita juga dapat mendeklaasikan 2 atau lebih variabel dengan nilai yang sama dalam 1 line
#dengan menggunakan = pada saat deklarasi variabel yang artinya
#variabel x sama dengan variabel y dan variabel dengan variabel z yang mana nilai dari variabel tersebut berupa "hallow"
x = y = z = "hallow"
