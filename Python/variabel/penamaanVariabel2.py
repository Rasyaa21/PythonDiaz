#penamaan variabel
#dalam penamaan variabel tidak bisa sembarangan kita harus mengikuti case style dalam programming
#dalam programming ada 4 tipe case style

# 1. Camel Case
# camel case ini adalah bentuk penamaan dari variabel yang dimana kata pertaman diawali dengan huruf kecil dan kata kedua menggunakan diawali
# #dengan huruf kapital seperti namanya sendiri camel case yang mirip dengan punggung unta
namaDepan = "Fauzan"

# 2. Snake Case
# snake case sendiri sangat populer bagi pemula dimana penamaan dari variabel ini menggukan underscore (_) sebagai pemisal antara kata pertama
#contoh dari pengunaan snake case
nama_belakang = "Suhada"

# Pascal Case
# pacal case sendiri mirip dengan camel case jika camel case pada kata pertaman menggunakan huruf kecil di camel case sendiri kata pertama dan kata
# selanjutnya diawali dengan huruf kapital
NamaLengkap = namaDepan + nama_belakang

print(NamaLengkap)
